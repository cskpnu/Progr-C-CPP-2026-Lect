#include <iostream>

int factorial(int n) {
    // 1. БАЗОВИЙ ВИПАДОК (Умова зупинки)
    // Якщо дійшли до 0 або 1, факторіал дорівнює 1. Більше функцію не викликаємо!
    if (n <= 1) {
        return 1;
    }

    // 2. РЕКУРСИВНИЙ КРОК
    // Функція викликає саму себе, але передає число на 1 менше (n - 1)
    return n * factorial(n - 1);
}

int main() {
    std::cout << "Факторіал 4 дорівнює: " << factorial(4) << std::endl;
    return 0;
}
