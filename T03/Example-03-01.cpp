#include <iostream>

using namespace std;

int main() {
    int var = 55;       // Звичайна змінна типу int
    int *ptr;           // Оголошення вказівника на int

    ptr = &var;         // Записуємо адресу змінної var у вказівник ptr

    // Виведення адрес
    cout << "Адреса змінної var (&var): " << &var << endl;
    cout << "Значення вказівника ptr:   " << ptr << endl;

    // Виведення значень
    cout << "Значення змінної var:      " << var << endl;
    cout << "Значення через вказівник (*ptr): " << *ptr << endl;

    // Зміна значення через вказівник
    *ptr = 100;         // Записуємо 100 за адресою, яку зберігає ptr

    cout << "\nНове значення var після зміни через вказівник: " << var << endl;

    return 0;
}
