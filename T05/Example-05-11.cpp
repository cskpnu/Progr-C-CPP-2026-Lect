#include <iostream>

// Створюємо структуру
struct DemoStruct {
    double d; // займає 8 байтів
    int i;    // займає 4 байти
    char c;   // займає 1 байт
};
// Загальний розмір структури: мінімум 13 байтів (в реальності часто 16 через вирівнювання)

// Створюємо об'єднання
union DemoUnion {
    double d; // займає 8 байтів
    int i;    // займає 4 байти
    char c;   // займає 1 байт
};
// Загальний розмір об'єднання: рівно 8 байтів (бо найбільше поле - double)

int main() {
    std::cout << "Розмір структури: " << sizeof(DemoStruct) << " байтів\n";
    std::cout << "Розмір об'єднання: " << sizeof(DemoUnion) << " байтів\n";

    return 0;
}
